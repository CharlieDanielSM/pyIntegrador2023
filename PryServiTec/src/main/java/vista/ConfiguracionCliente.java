    private void txtuserKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtuserKeyReleased
        String nombreuser= txtuser.getText();
        if(nombreuser.equalsIgnoreCase(empleador.getNombEmpl())){
            juser.setText("");
        }else{
            if(nombreuser.equals("")){
                juser.setText("");

            }else{
                if(usuarioverificar(nombreuser)==true){
                    juser.setText("NOMBRE USUARIO YA EXISTE");
                    juser.setForeground(Color.RED);
                    validacion=2;

                }else{
                    juser.setText("NOMBRE USUARIO VALIDO");
                    Color verdeOscuro = new Color(0, 128, 0);
                    juser.setForeground(verdeOscuro);
                    validacion=1;
                }
            }
        }
        int X=txtuser.getText().length();
        if(X>11){
            txtuser.setText(txtuser.getText().substring(0, 9));

        }
    }//GEN-LAST:event_txtuserKeyReleased

